/**
 * hdmi_inl
 * Columbus hdmi register read
 *
 * @param <addr>	{ addr of register }
 * @return 		{ read value }
 * @ingroup drv_hdmi
 */

UINT8 hdmiport_in(UINT8 addr)
{
    UINT8 result;
    rtdf_pageSelect(0x02);	// according to spec
    hdmi_out(HDMI_HDMI_AP_VADDR, addr);
    result = (UINT8) (hdmi_in(HDMI_HDMI_DP_VADDR) & 0xFF);
    return result;
}

/**
 * hdmi_outl
 * Columbus hdmi register write
 *
 * @param <addr>	{ addr of register }
 * @param <value>	{ write value }
 * @return
 * @ingroup drv_hdmi
 */

void hdmiport_out(UINT8 addr, UINT8 value)
{

    rtdf_pageSelect(0x02);	// according to spec
    hdmi_out(HDMI_HDMI_AP_VADDR, addr);
    hdmi_out(HDMI_HDMI_DP_VADDR, value);
}

/**
 * hdmi_maskl
 * Columbus register read and mask function
 *
 * @param <addr>	{ register addr }
 * @param <andmask> { andmask AND with register value }
 * @param <ormask>  { ormask OR with register value }
 * @return
 * @ingroup drv_hdmi
 */
void hdmiport_mask(UINT8 addr, UINT8 andmask, UINT8 ormask)
{
    UINT8 result;
    rtdf_pageSelect(0x02);	// according to spec
    hdmi_out(HDMI_HDMI_AP_VADDR, addr);
    result = (UINT8) (hdmi_in(HDMI_HDMI_DP_VADDR) & 0xFF);
    result &= andmask;
    result |= ormask;
    hdmi_out(HDMI_HDMI_AP_VADDR, addr);
    hdmi_out(HDMI_HDMI_DP_VADDR, result);

}


